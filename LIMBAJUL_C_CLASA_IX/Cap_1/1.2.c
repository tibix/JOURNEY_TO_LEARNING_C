/* Vocabularul Limbajului */
/*
- cuvinte rezervate: sunt nume rezervate instructiunilor, tipurilor fundamentale si sintaxei de definire a functiilor si tipurilor de date
- identificatori: sunt numde de data constante sau variabile. Sunt formate dintr-un sir de caractere care incep cu o litera sau cu '_', urmatoarele caractere putind fii litere, cifre sau '_'
- constante: sunt valori fixe reprezentand caractere, siruri de caractere, numere intregi sau rationale
- delimitatori: reprezinta simboluri care separa entiatile (spatiu tab etc.)
OBS: limbajul C este "case sensitive", adica face diferenta intre A si a. Ex: int mama != int Mama
*/