/*
In C valorile pot fi stocate in constante si variabile.
Constantele nu isi modifica valoare pe durata executie si se definesc printr-o directiva de preprocesare numita macrodefinitie, astfel:

#define OK 1 //constant simbolica (pentru care are nume)

Variabilele isi pot modifica valoare pe parcursul executiei
Orice variabila trebuie declarata inainte de a fi folosita

Tipurile de date poti fi predefinite (exista deja in limbaj) sau derivate (sunt create de programator - Ex: tipul de data data_calendaristica)

Clasificare alternativa a tipurilor de date:
- simple (scalare) - contin o singura valoare de un anumit tip
- compuse - contin mai multe valori de acelasi tip, sau tipuri diferite
- pointeri - contin adrese de memorie ale unor entitati
*/